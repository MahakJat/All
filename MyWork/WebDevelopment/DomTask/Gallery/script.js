let image_container  = document.querySelector(".image-container");
const lightbox = document.getElementById('lightbox');
const lightboxImage = document.getElementById('lightbox-img');
const closeButton = document.querySelector('.close');

let clutter = " ";

arr.forEach(function(elem){
   clutter += `
            <img class ="img w-[20vh] h-[20vh] bg-black shrink-0 mt-0" src=${elem} alt="">
      ` 
})
image_container.innerHTML = clutter;


const galleryItems = document.querySelectorAll('.img');
galleryItems.forEach(function(image){
    image.addEventListener("click" ,function(e){
        lightboxImage.src = e.target.src;
        lightbox.style.display = 'flex';
    })
})

closeButton.addEventListener('click', () => {
    lightbox.style.display = 'none';
});

